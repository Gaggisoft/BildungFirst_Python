# Aufgabe: Arbeiten mit Strings und Anführungszeichen

# Formuliere die folgenden Sätze mit print() in Python.
# Achte darauf, die passenden Anführungszeichen zu verwenden
# oder Escape-Zeichen (\) zu setzen, wo es nötig ist.

# 1) Orion sagt: "Heute lerne ich Python."
print('Orion sagt: "Heute lerne ich Python."')
print("Orion sagt: \"Heute lerne ich Python.\"")
# 2) It's Orion's favorite tool.
print("It's Orion's favorite tool.")
