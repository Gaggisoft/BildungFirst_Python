print('42')
# 42 wird als Text interpretiert
# Text wird ausgegeben: 42

print(42)
# 42 wird als Zahl (Integer) interpretiert
# Python gibt Zahlenwert aus

print('100+55')
# 100+55 wird als Text interpretiert
# Python gibt den Text aus: 100+55

print(100+55)
# 100 und 55 werden als Zahlen interpretiert
# Summe wird berechnet
# Zahl (Summe) wird ausgegeben

