# 3) "Don't worry," meinte Lyra.
print('"Don\'t worry," meinte Lyra.')
print('\"Don\'t worry,\" meinte Lyra.')
# 4) Ava flüstert: "Let's code together."
print('Ava flüstert: "Let\'s code together."')
print('Ava flüstert: \"Let\'s code together.\"')
